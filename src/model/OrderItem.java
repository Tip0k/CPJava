package model;

public class OrderItem {

    private String item;
    private int Wg;
    private int Wcm;
    private int Hcm;
    private int Lcm;

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }

    public int getWg() {
        return Wg;
    }

    public void setWg(int Wg) {
        this.Wg = Wg;
    }

    public int getWcm() {
        return Wcm;
    }

    public void setWcm(int Wcm) {
        this.Wcm = Wcm;
    }

    public int getHcm() {
        return Hcm;
    }

    public void setHcm(int Hcm) {
        this.Hcm = Hcm;
    }

    public int getLcm() {
        return Lcm;
    }

    public void setLcm(int Lcm) {
        this.Lcm = Lcm;
    }
}
